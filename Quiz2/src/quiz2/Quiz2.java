    private void DaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DaftarActionPerformed
    boolean isValid = true;
    if (NIK.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "NIK harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        isValid = false;
    }
    if (Nama.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nama harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
         isValid = false;
    }
    if (Alamat.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Alamat harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
         isValid = false;
    }
 
        if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected()) {
        JOptionPane.showMessageDialog(this, "Jenis kelamin harus dipilih!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        isValid = false;
    }

    if (!Makan.isSelected() && !Minum.isSelected() && !TidakKeduanya.isSelected()) {
        JOptionPane.showMessageDialog(this, "Hobby harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        isValid = false;
    }
    
    Object PdItem = Pendidikan.getSelectedItem();
    if (PdItem == null) {
        JOptionPane.showMessageDialog(this, "Pendidikan harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
         isValid = false;
      
    } else if (PdItem.equals("SD") || PdItem.equals("SMP") || PdItem.equals("SMA")) {
        JOptionPane.showMessageDialog(this, "Pendidikan tidak memenuhi syarat", "Peringatan", JOptionPane.WARNING_MESSAGE);
         isValid = false;
        
    }
    String input = IPK.getText();
    double ipk = Double.parseDouble(input);
    if(ipk <3.01){
        JOptionPane.showMessageDialog(this, "IPK minimal 3.01", "Peringatan", JOptionPane.WARNING_MESSAGE);
        
         isValid = false;
    }
        
    
  

    
  
    if(isValid){
    JOptionPane.showMessageDialog(this, "Pendaftaran berhasil!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
    }

        
    
    }//GEN-LAST:event_DaftarActionPerformed
    private int hitungUmur(Date tanggalLahir){
        Date TanggalSekarang = new Date();
        long selisih =TanggalSekarang.getTime() - tanggalLahir.getTime();
        return (int) (selisih/ (1000L  *60 *60 *24*365));
    }
