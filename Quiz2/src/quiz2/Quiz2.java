    private void TanggalLahirFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_TanggalLahirFocusLost
       String input = TanggalLahir.getText();
    try {
    Date tanggalLahir = new SimpleDateFormat("dd-MM-yyyy").parse(input);
    int umur = hitungUmur(tanggalLahir);
    if (umur < 18) {
        JOptionPane.showMessageDialog(this, "Tidak cukup umur", "Error", JOptionPane.ERROR_MESSAGE);
        TanggalLahir.setText("");  // Kosongkan jika usia tidak valid
    }
    } catch (ParseException e) {
    JOptionPane.showMessageDialog(this, "Format tanggal tidak valid", "Peringatan", JOptionPane.WARNING_MESSAGE);
    TanggalLahir.setText("");
    }
    }//GEN-LAST:event_TanggalLahirFocusLost
        
